
public class Exer6 {

	public static void main(String[] args) {
		byte menuByte = 10;
		// byte para short
		short menuShort = menuByte;
		System.out.println(menuShort);
		
		// short para int
		int menuInt = menuShort;
		System.out.println(menuInt);
		
		// int para float
		float menuFloat = menuInt;
		System.out.println(menuFloat);
		
		// float para double
		double menuDouble = menuFloat;
		System.out.println(menuDouble);

		// maior não cabe no menor.
		// byte v1 = 128;
		// meuByte = meuInt;
	}

}
